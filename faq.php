							const itemText = $(this).text().toLowerCase();
							if(itemText.indexOf(searchText) > -1) {
								$(this).show();
								
								// Open the matching FAQ items
								const content = $(this).find('.faq-content');
								const head = $(this).find('.faq-head');
								
								head.addClass('active');
								content.addClass('show');
								
								// Highlight search terms (optional enhancement)
								// This is a placeholder for actual highlighting functionality
							} else {
								$(this).hide();
							}
						});
					} else if(searchText.length === 0) {
						// If search is cleared, show all and collapse
						$('.faq-item').show();
						$('.faq-head').removeClass('active');
						$('.faq-content').removeClass('show');
					}
				});
				
